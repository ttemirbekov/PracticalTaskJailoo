package com.company;

public class Ui {
    private String aty;
    private int jashy;
    private int salmagy;



    public Ui(String aty, int jashy, int salmagy) {
        this.aty = aty;
        this.jashy = jashy;
        this.salmagy = salmagy;
    }

    public String getAty() {
        return aty;
    }

    public void setAty(String aty) {
        this.aty = aty;
    }

    public int getJashy() {
        return jashy;
    }

    public void setJashy(int jashy) {
        this.jashy = jashy;
    }

    public int getSalmagy() {
        return salmagy;
    }

    public void setSalmagy(int salmagy) {
        this.salmagy = salmagy;
    }
}
